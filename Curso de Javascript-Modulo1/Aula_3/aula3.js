// operadores aritimeticos
// adição +
var sumTwoNumbers = 1 + 3
console.log(sumTwoNumbers);
// subtração -
var subTwoNumbers = 4-3
console.log(subTwoNumbers);
// multilicação *
var mulTwoNumbers = 4*3
console.log(mulTwoNumbers);
// divisão /
var divTwoNumbers = 4/2
console.log(divTwoNumbers);
// modulo ou resto de uma divisão %
console.log(8%3);
// incremento ++
var num = 1;
console.log(++num);
// decremento --
console.log(--num);
//atribuição de soma
var x = 1
var y = 3
console.log(x+=y);
//atribuição de subtração
console.log(x-=y);
//atribuição de multiplicação
console.log(x*=y);
//atribuição de divisão
console.log(x/=y);
//atribuição de resto da divisão
console.log(x%=y);

